package array;

public class TypeCasting {
    public static void main(String[] args) {
        //widening type casting
        byte a= 123;
        short b;
        b=a;

        //Narrow type casting
        a=(byte) b;
        System.out.println(a);

        //exampels
        int x;
        long y=163563487346994l;

        x=(int)y;
        System.out.println(x);



        


    }
}
