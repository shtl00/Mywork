package operators;

public class Assignment {
    public static void main(String[] args) {
        // +=
        int a= 10;
        a=20;
        a+=10;
        a++;
        System.out.println(a);

        // -=
        int b= 10;
        b =20;
        b-= 5;
        b--;
        System.out.println(b);

        // *=
        int c= 20;
        c= 30;
        c*=10;
        System.out.println(c);

        // /=
        float d=10;
        d= 50;
        d/= 10;
        System.out.println(d);

        // %=
       float e= 100;
       e= 500;
       e%=10;
        System.out.println(e);


    }
}
